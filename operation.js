var x = 80;
var y = 7;

// Addition
var addition = x + y;
console.log(addition);

// Subtraction
var subtraction = x - y;
console.log(subtraction);

// Multiplication
var multiplication = x * y;
console.log(multiplication);

// Division
var division = x / y;
console.log(division);

// Reminder/Modulus
var modulus = x % y;
console.log(modulus);

/*
Here,
x & y are called operand
+, -, *, /, % are called operator
*/